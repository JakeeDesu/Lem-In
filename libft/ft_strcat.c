#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t l1;
	size_t l2;
	size_t i;

	l1 = ft_strlen(s1);
	l2 = ft_strlen(s2);
	i = 0;
	while (i <= l2)
		s1[l1++] = s2[i++];
	return (s1);
}
