#include "libft.h"

char	*ft_strmapi(char *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			l;
	char			*s1;

	l = ft_strlen(s);
	if (!(s1 = (char *)malloc(sizeof(char) * l + 1)))
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		s1[i] = f(i, s[i]);
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
