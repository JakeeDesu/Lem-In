#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t l1;
	size_t i;

	l1 = ft_strlen(s1);
	i = 0;
	while (i < n && s2[i] != '\0')
		s1[l1++] = s2[i++];
	s1[l1] = '\0';
	return (s1);
}
